#include <QtGui>
#include "mainwindow.h"
#include "edit.h"
#include "globals.h"
#include "transform.h"

EditMenu::EditMenu(MainWindow* mainWin)

{  
    this->mainWindow = mainWin;
    this->setTitle(tr("Edit"));
    createActions();
    createMenus();
}


void EditMenu::createMenus()
{
    this->setTearOffEnabled(true);
    this->addMenu(new TransformMenu(mainWindow));
    this->addSeparator();
    this->addAction(cutAct);
    this->addAction(copyAct);
    this->addAction(pasteAct);
}

void EditMenu::createActions()
{
   
    cutAct = new QAction(QIcon(":/images/cut.png"), tr("Cu&t"), this);
    cutAct->setShortcuts(QKeySequence::Cut);
    cutAct->setStatusTip(tr("Cut the current selection's contents to the "
                            "clipboard"));
//     connect(cutAct, SIGNAL(triggered()), mainWindow->textEdit, SLOT(cut()));

    copyAct = new QAction(QIcon(":/images/copy.png"), tr("&Copy"), this);
    copyAct->setShortcuts(QKeySequence::Copy);
    copyAct->setStatusTip(tr("Copy the current selection's contents to the "
                             "clipboard"));
//     connect(copyAct, SIGNAL(triggered()), mainWindow->textEdit, SLOT(copy()));

    pasteAct = new QAction(QIcon(":/images/paste.png"), tr("&Paste"), this);
    pasteAct->setShortcuts(QKeySequence::Paste);
    pasteAct->setStatusTip(tr("Paste the clipboard's contents into the current "
                              "selection"));
//     connect(pasteAct, SIGNAL(triggered()), mainWindow->textEdit, SLOT(paste()));
  
    
    cutAct->setEnabled(false);
    copyAct->setEnabled(false);
}

void EditMenu::autoScale()
{
   autoscale_proc(); 
}



